    /// Easing In: the changes happen slowly at first, but then speed up toward the end
    /// </summary>
    EasingIn,

    /// <summary>
    /// Quad In
    /// </summary>
    QuadIn,

    /// <summary>
    /// Quad Out
    /// </summary>
    QuadOut,

    /// <summary>
    /// Quad In/Out
    /// </summary>
    QuadInOut,

    /// <summary>
    /// Cubic In
    /// </summary>
    CubicIn,

    /// <summary>
    /// Cubic Out
    /// </summary>
    CubicOut,

    /// <summary>
    /// Cubic In/Out
    /// </summary>
    CubicInOut,

    /// <summary>
    /// Quart In
    /// </summary>
    QuartIn,

    /// <summary>
    /// Quart Out
    /// </summary>
    QuartOut,

    /// <summary>
    /// Quart In/Out
    /// </summary>
    QuartInOut,

    /// <summary>
    /// Quint In
    /// </summary>
    QuintIn,

    /// <summary>
    /// Quint Out
    /// </summary>
    QuintOut,

    /// <summary>
    /// Quint In/Out
    /// </summary>
    QuintInOut,

    /// <summary>
    /// Sine In
    /// </summary>
    SineIn,

    /// <summary>
    /// Sine Out
    /// </summary>
    SineOut,

    /// <summary>
    /// Sine In/Out
    /// </summary>
    SineInOut,

    /// <summary>
    /// Expo In
    /// </summary>
    ExpoIn,

    /// <summary>
    /// Expo Out
    /// </summary>
