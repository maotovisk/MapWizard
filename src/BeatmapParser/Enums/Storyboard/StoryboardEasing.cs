namespace MapWizard.BeatmapParser;
/// <summary>
/// Represents the easing functions used in a storyboard.
/// </summary>
public enum Easing
{
    /// <summary>
    /// Linear: no easing
    /// </summary>
    Linear,

    /// <summary>
