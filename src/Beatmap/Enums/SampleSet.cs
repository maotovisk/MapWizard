namespace Beatmap;

/// <summary>
/// Represents a sample set.
/// </summary>
public enum SampleSet : uint
{

    /// <summary>
    ///  Default beatmap sample set.
    /// </summary>
    Default = 0,

    /// <summary>
    ///  Normal sample set.
    /// </summary>
    Normal = 1,

    /// <summary>
    ///  Soft sample set.
    /// </summary>
    Soft = 2,

    /// <summary>
    ///  Drum sample set.
    /// </summary>
    Drum = 3,
}